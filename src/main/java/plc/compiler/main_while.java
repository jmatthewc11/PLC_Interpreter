public final class Main {

    public static void main(String[] args) {
        INTEGER i = 2;
        INTEGER n = 5;
        INTEGER zero;
        while (n != 0) {
            zero = n - i * (n / i);
            if (zero != 1) {}
            if (zero != 1) {
                PRINT("even");
            }
            if (zero != 1) {} else {
                PRINT("odd");
            }
            if (zero != 1) {
                PRINT("even");
            } else {
                PRINT("odd");
            }
            n = n - 1;
        }
    }

}